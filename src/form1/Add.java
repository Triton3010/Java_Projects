    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean b=true;
 if(jTextField1.getText().trim().length() == 0 || jTextField2.getText().trim().length() == 0 || jTextField3.getText().trim().length() == 0 ||jTextField4.getText().trim().length() == 0 || jTextField5.getText().trim().length() == 0)
 {
     JOptionPane.showMessageDialog(this, "All fields are required");
     b = false;
 }
      
    if(!jTextField1.getText().matches("[0-9]*")) {
     JOptionPane.showMessageDialog(this, "Enter a numeric value in the proper field");
     b = false;
    }
    if(!jTextField4.getText().matches("[0-9]*")) {
     JOptionPane.showMessageDialog(this, "Enter a numeric value in the proper field");
     b = false;
    }
     int empno = Integer.parseInt(jTextField1.getText());
     int sal = Integer.parseInt(jTextField4.getText());  
      String name = jTextField2.getText();
      String des = jTextField3.getText();
      String dep = jTextField5.getText();
      
      int r=0;
      
     if(b)    
     {
         try
          {
             Class.forName("com.mysql.jdbc.Driver");
             Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pro","root","");
             PreparedStatement pst = con.prepareStatement("insert into t2 values (?,?,?,?,?)");
             pst.setInt(1,empno);
             pst.setString(2,name);
             pst.setString(3,des);
             pst.setInt(4,sal);
             pst.setString(5,dep);
             r =pst.executeUpdate();
             con.close();

          }
        catch(Exception e)
        {
            System.out.println("Exception Caught");
        }   }
        if(r>0)
        JOptionPane.showMessageDialog(this, "Record was successfully entered");
        if(r==0) 
        JOptionPane.showMessageDialog(this, "Record cannot be inserted");

    }//GEN-LAST:event_jButton1ActionPerformed

