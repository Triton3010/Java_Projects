    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      boolean b=true;
 if(d1.getText().trim().length() == 0)
 {
     JOptionPane.showMessageDialog(this, "Fill the field");
     b = false;
 }
      
    if(!d1.getText().matches("[0-9]*")) {
     JOptionPane.showMessageDialog(this, "Enter a numeric value");
     b = false;
    }
    
     int empno = Integer.parseInt(d1.getText());
          
     int r=0;
      
     if(b)    
     {
         try
          {
             Class.forName("com.mysql.jdbc.Driver");
             Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pro", "root","" );
             PreparedStatement pst = con.prepareStatement("delete from t2 where empno=?");
             pst.setInt(1,empno);
            
             r =pst.executeUpdate();
             con.close();

          }
        catch(Exception e)
        {
            System.out.println("Exception Caught");
        }   }
        if(r>0)
        JOptionPane.showMessageDialog(this, "Record was successfully deleted");
        else if(r==0)
        JOptionPane.showMessageDialog(this, "No such record exists");

    
    }//GEN-LAST:event_jButton1ActionPerformed

